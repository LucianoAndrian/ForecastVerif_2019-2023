def get_mask(data, path):
    import numpy as np
    lat, lon = data['lat'].values, data['lon'].values
    x, y = np.meshgrid(lon, lat)
    points = np.column_stack((y.ravel(), x.ravel()))
    mask = path.contains_points(points).reshape(x.shape)

    return data.where(mask)
