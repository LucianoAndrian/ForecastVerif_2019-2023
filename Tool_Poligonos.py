def Set_LonLat(coordenadas_str):
    coordenadas_pares = coordenadas_str[1:-1].split(", ")

    coordenadas_ordenadas = [
        (float(coordenada.split()[1]), float(coordenada.split()[0]) + 360) for
        coordenada in coordenadas_pares]

    return coordenadas_ordenadas

